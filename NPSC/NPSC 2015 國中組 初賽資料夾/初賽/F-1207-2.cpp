#include<stdio.h>
#include<string.h>
int main()
{
	int n,z,i,cal[100000],b,sum,hun,ten,yn,len,s,d,sin;
	char input[100001];
	
	scanf("%d",&n);
	for(z=0;z<n;z++)
	{
		scanf("%s%d",input,&b);
		yn=0;
		sum=0;
		len=strlen(input);
		for(i=0;i<len;i++)
		{
			cal[i]=input[i]-'0';
		}
		ten=10*cal[len-2]+cal[len-1];
		hun=100*cal[len-3]+10*cal[len-2]+cal[len-1];
		sin=cal[len-1];
		printf("%d %d",ten,hun);
		for(i=0;i<len;i++)
		{
			sum=sum+cal[i];
		}
		
		if(b==2)
		{
			if(cal[len-1]%2==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==3)
		{
			if(sum%3==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==4)
		{
			if(len==1)
			{
				if(sin%4==0)
				{
					printf("YES\n");
					yn=1;
				}
			}
			else
			{
				if(ten%4==0)
				{
					printf("YES\n");
					yn=1;
				}
			}
			
		}
		if(b==5)
		{
			if(cal[len-1]%5==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==6)
		{
			if(cal[len-1]%2==0&&sum%3==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==8)
		{
			if(len==1)
			{
				if(sin%8==0)
				{
					printf("YES\n");
					yn=1;
				}
			}
			else if(len==2)
			{
				if(ten%8==0)
				{
					printf("YES\n");
					yn=1;
				}
			}
			else
			{
				if(hun%8==0)
				{
					printf("YES\n");
					yn=1;
				}
			}
		}
		if(b==9)
		{
			if(sum%9==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==10)
		{
			if(cal[len-1]%10==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(b==11)
		{
			s=0;
			d=0;
			for(i=0;i<len;i=i+2)
			{
				s=s+cal[i];
			}
			for(i=0;i<len;i=i+2)
			{
				d=d+cal[i];
			}
			if((d-s)%11==0)
			{
				printf("YES\n");
				yn=1;
			}
		}
		if(yn==0)
		{
			printf("NO\n");
		}
		
	}
}
